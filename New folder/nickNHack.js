const findCoins = (n, k) => {
  if (n === k) return true;
  if (n > k) return false;
  return findCoins(n * 10, k) || findCoins(n * 20, k);
};

const runProgram = (input) => {
  input = input.trim().split("\n");
  let cases = +input[0],
    line = 1;
  for (let i = 0; i < cases; i++) {
    let n = +input[line++];
    findCoins(1, n) ? console.log("Yes") : console.log("No");
  }
};

