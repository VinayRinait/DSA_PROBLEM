function findit(array, n) {
  let obj = {};
  for (let i = 0; i < n; i++) {
    if (obj[array[i]] == undefined) obj[array[i]] = 1;
    else obj[array[i]]++;
  }
  for (key in obj) {
    if (obj[key] > 1) return "Yes";
  }
  return "No";
}

