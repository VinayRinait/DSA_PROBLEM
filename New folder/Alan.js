const findit = (str) => {
  let res = [];
  for (let i = 0; i < str.length; i++) {
    if (str[i] != "#") res.push(str[i]);
    else res.pop();
  }
  console.log(res.join(""));
};

function runProgram(input) {
  input = input.trim().split("\n");
  let cases = +input[0];
  let line = 1;
  for (let i = 0; i < cases; i++) {
    let str = input[line++].trim();
    findit(str);
  }
}


