function Recursive(array, n) {
  if (n == 0) return;
  for (let i = 0; i < n - 1; i++) {
    if (array[i] > array[i + 1]) {
      [array[i], array[i + 1]] = [array[i + 1], array[i]];
    }
  }
  Recursive(array, n - 1);
}

function runProgram(input) {
  input = input.trim().split("\n");
  let n = +input[0];
  let array = input[1].trim().split(" ").map(Number);
  Recursive(array, n);
  console.log(array.join(" "));
}
