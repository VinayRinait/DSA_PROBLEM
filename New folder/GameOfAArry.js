function runProgram(input) {
  input = input.trim().split("\n");
  let [n, k] = input[0].trim().split(" ").map(Number);
  let array = input[1].trim().split(" ").map(Number);
  let res = "";
  if (k > array.length) k = k % n;
  for (let i = k; i < n; i++) {
    res += array[i] + " ";
  }
  for (let i = 0; i < k; i++) {
    res += array[i] + " ";
  }

  console.log(res);
}
